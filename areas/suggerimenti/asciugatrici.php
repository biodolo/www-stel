<h3>PROBLEMI DI FUNZIONAMENTO</h3>
<div>
	<br>
	<b>Tempo di asciugatura troppo lungo</b>
	<p>
		Non &egrave; un guasto della tua asciugabiancheria, i sensori hanno rilevato una temperatura ambiente troppo bassa e il controllo elettronico ha adeguato il tempo previsto di asciugatura. Quando la temperatura dell'ambiente dove hai installato la tua asciugabiancheria torner&agrave; sopra i 10 °C il tempo indicato torner&agrave; normale. Comunque non temere durante il ciclo il tempo viene corretto ed adeguato alle effettive esigenze.
	</p><br>
	<b>Rumore inusuale</b>
	<p>
		Se di nuova installazione, il rumore potrebbe essere causato dalla pompa che deve essere innescata con un po' d'acqua, dal sensore di umidit&agrave; o dal feltro. Se l'asciugabiancheria &egrave; nuova, &egrave; richiesto un periodo di rodaggio.
	</p>
	<p><br>
		Ronzio: il motore potrebbe incepparsi od avere difficolt&agrave; a far muovere il cestello. &Egrave; possibile che l'apparecchiatura sia sovraccarica. Rimuovere alcuni indumenti.
	</p>
	<i>Se il rumore persiste, l'apparecchiatura deve essere controllata da un tecnico.</i>
	<p><br>
		Bip: il rumore può essere causato dal sensore umidit&agrave; o dal nastro di feltro. Se l'asciugabiancheria &egrave; nuova, &egrave; richiesto un periodo di rodaggio.
	</p>
	<p><br>
		Rumore metallico: la ventola dell'aria può essere allentata. <i>Per il controllo &egrave; necessario l'intervento di un tecnico.</i>
	</p>
	<p><br>
		Cigolio: il rumore può essere causato dal sensore umidit&agrave; o dal nastro di feltro. Se l'asciugabiancheria &egrave; nuova, &egrave; richiesto un periodo di rodaggio.
	</p><br>
	<b>Scarsi risultati di asciugatura</b>
	<p>
		Controllare se la regolazione della durezza dell'acqua &egrave; corretta: Alcuni modelli dispongono della regolazione per la durezza dell'acqua. Questa regolazione influisce direttamente sulla misurazione dell'umidit&agrave; e quindi anche sul risultato dell'asciugatura finale. Controllare il manuale delle istruzioni per vedere se l'apparecchiatura in oggetto &egrave; dotata di tale regolazione e se questa &egrave; impostata correttamente. Pulire il cestello, perché un cestello sporco può causare problemi al sensore dell'umidit&agrave;. In particolare l'ammorbidente può lasciare dei residui nel cestello. Il cestello ed i sensori di umidit&agrave; possono essere puliti con aceto.
	</p><br>
	<b>Impiega troppo tempo ad asciugare</b>
	<p>
		Verificare innanzitutto la velocit&agrave; di centrifuga della lavatrice che deve essere di almeno 1000 giri, velocit&agrave; inferiori lasciano la biancheria troppo umida . Se la tua lavatrice centrifuga ad una velocit&agrave; inferiore a 1000 giri purtroppo devi sostituirla. Pulisci i filtri, filtri sporchi ostacolano il funzionamento. Se la tua asciugabiancheria funziona a pompa di calore ricorda che deve essere installato in ambiente con temperatura superiore a 10 °C, con temperatura esterna inferiore le sue prestazioni diminuiscono sensibilmente.
	</p><br>
	<b>Si accende o lampeggia il led del contenitore dell'acqua</b>
	<p>
		Il LED del serbatoio dell'acqua &egrave; sempre acceso alla fine del programma di asciugatura, per ricordare che occorre svuotarlo dopo ogni ciclo. &Egrave; normale che il LED del serbatoio dell'acqua sia acceso dopo il primo ciclo di asciugatura anche se non c'&egrave; acqua nel serbatoio. L'acqua viene prima raccolta in un grande contenitore e solo successivamente viene scaricata nel serbatoio dell'acqua.
		Se il LED &egrave; ancora acceso dopo aver spento e riacceso l'apparecchiatura, o se si accende prima di ogni programma, c'&egrave; dell'acqua nel serbatoio di troppopieno. Può darsi che il condensatore debba essere pulito (vedere il manuale delle istruzioni).
		Se l'asciugatrice &egrave; collegata allo scarico tramite un tubo, può darsi che non funzioni correttamente, se il tubo &egrave; attorcigliato oppure se non &egrave; stato accorciato.
	</p><br>
	<b>Sul display appare un'icona a forma di lucchetto</b>
	<p>
		&Egrave; stato attivato il blocco di protezione bambini. Questo evita che il programma possa essere involontariamente avviato o modificato.
		Lo spegnimento varia a seconda dell'apparecchiatura: per ulteriori informazioni, riferirsi al manuale delle istruzioni del relativo modello.
	</p><br>
	<b>Si accende la spia del condensatore</b>
	<p>
		Indica che il condensatore deve essere pulito regolarmente. Se l'apparecchiatura non asciuga adeguatamente, pulire il condensatore, i filtri ed i condotti dell'aria. <i>Se la pulizia non risolve il problema, il controllo e la riparazione devono essere eseguiti da un tecnico.</i>
	</p>
</div>

<h3>MANUTENZIONE</h3>
<div>
	<br>
	<b> Allungare la vita all'asciugatrice</b>
	<p>
		Per allungare la vita all'asciugatrice sono sufficienti pochi e piccoli accorgimenti.
		Grazie a questi non solo evitiamo malfunzionamenti, ma riduciamo anche l'impatto ambientale e risparmiamo sui consumi. Ecco cosa puoi fare per mantenere più a lungo la tua asciugatrice.
	</p>
	<p>
		Svuota il serbatoio dell'acqua.
	</p>
	<p>
		Di solito &egrave; in alto, a volte vicino allo sportello.
		L'acqua deve essere rimossa quando l'indicatore del serbatoio segnala il pieno.
		Un'altra possibilit&agrave; &egrave; quella di collegarlo direttamente allo scarico, sar&agrave; forse meno gradevole alla vista, ma più pratico.
		I produttori indicano dopo ogni asciugatura, comunque non aspettare mai più di 5 cicli.
	</p>
	<p>
		Pulisci del filtro.
	</p>
	<p>
		Bisogna rimuovere regolarmente i residui (lanugine e simili) perché ostacolano le prestazioni della macchina, facendole perdere efficienza e consumare di più. Si consiglia di farlo ogni 5 o 6 programmi.
	</p>
	<p>
		Controlla e pulisci il condensatore. In genere &egrave; situato nella parte inferiore della macchina. Bisogna estrarlo e lavarlo con acqua fredda. L'operazione &egrave; da eseguire con una certa frequenza (a meno che il modello abbia il sistema di autopulizia che sfrutta l’acqua di condensa accumulata), come minimo ogni 10-12 cicli di asciugatura.
