<h3>INSTALLAZIONE</h3>
<div>
	<br>
	<b>Suggerimenti per l'installazione dell'asciugabiancheria</b>
	<p>
		La macchina pu&ograve; essere posta su qualsiasi tipo di pavimento piano. Deve trovarsi in posizione perfettamente orizzontale (servirsi eventualmente di una livella ad acqua). Agendo sui piedini &egrave; possibile compensare dislivelli ed inclinazioni del pavimento. Per un corretto funzionamento l'ambiente circostante deve essere mantenuto pulito (sgombro da polvere e peluzzi). L'aria deve circolare liberamente attorno alla macchina. Non ostruire la griglia di aerazione anteriore e le griglie di aspirazione sul retro. Non togliere i piedini. Non limitare la distanza dal pavimento con moquette o con tappeti con pelo alto. Ci&ograve; potrebbe provocare un accumolo di calore che pregiudicherebbe il buon funzionamento della macchina. Nota: L'aria calda emessa dalla macchina pu&ograve; raggiungere anche i 60°C. Pertanto non installare la macchina su pavimenti che non possono sopportare temperature elevate. La temperatura ambiente non deve superare i 35°C anche quando la macchina &egrave; in funzione. In caso contrario aprire la porta o la finestra.
	</p>
	<p>
		Prima di inserire la spina nella presa accertarsi che il valore della tensione di alimentazione dell'impianto elettrico sia conforme al valore indicato nelle caratteristiche tecniche e che il contatore, i fusibili, la linea di alimentazione e la presa di corrente siano dimensionati per sopportare il carico massimo richiesto. Le norme antinfortunistiche prevedono che la macchina sia collegata con una efficace presa di terra. La casa costruttrice declina ogni responsabilit&agrave; qualora questa norma antinfortunistica non venga rispettata.
	</p>
	<p>
		Se il luogo dove &egrave; installato l’asciugabiancheria ha un condotto di scarico a terra, l’acqua di condensa pu&ograve; essere evacuata tramite un tubicino, facilmente reperibile in commercio. Se l’asciugabiancheria &egrave; installato sopra la lavatrice, l’acqua di condensa pu&ograve; essere evacuata ugualmente tramite il tubicino , tenendo presente che il tubicino deve essere orientato verso il basso.
	</p>
	<b> Posso installare la mia asciugatrice all'esterno?</b>
	<p>
		Se la tua asciugabiancheria funziona a pompa di calore deve essere installata in ambiente con temperatura superiore a 10 °C, con temperatura inferiore il suo rendimento diminuisce notevolmente e con temperatura di 5 °C smette di funzionare. Perciò evita di installarlo all'esterno o in ambiente dove la temperatura scenda sotto i 10 °C.
	</p>
	<b>Posso invertire il senso dell'apertura dello sportello?</b>
	<p>
		&egrave; possibile invertire il senso di apertura dello sportello delle asciugabiancheria, questa operazione non rientra nella garanzia.
	</p>
</div>

<h3>CONSIGLI DI UTILIZZO</h3>
<div>
	<br>
	<b>Suggerimenti per utilizzo ottimale dell'asciugabiancheria</b>
	<p>
		Per il corretto funzionamento dell'asciugabiancheria la biancheria introdotta nel cesto deve necessariamente essere stata centrifugata ad una velocit&agrave; di almeno 650 giri. Se centrifugata a velocit&agrave; inferiore non verranno rispettati i tempi di asciugatura che possono alllungarsi all'infinito; si produrranno anche delle inevitabili perdite di acqua con pericolo di corto circuito. Se la tua lavatrice centrifuga ad una velocit&agrave; inferiore sconsigliamo l'uso dell'asciugabiancheria.
	</p>
	<b>Lampada spia tanica piena</b>
	<p>
		Si accende quando la tanica &egrave; piena o non &egrave; ben inserita. Il programma di asciugatura si interrompe. Importante: Se durante il ciclo di asciugatura la tanica si riempie completamente, la rispettiva lampada spia si accende, indicando che la tanica deve essere svuotata. Il selettore tempi di asciugatura continuer&agrave; a ruotare portandosi progressivamente sullo “O”, ma l’asciugatura non avr&agrave; luogo. In tal caso, dopo aver svuotato la tanica, selezionare nuovamente il ciclo di asciugatura per il tempo necessario a raggiungere il grado di asciugatura desiderato. Attenzione: Dopo tale operazione, l’asciugabiancheria pu&ograve; rimanere alcuni minuti ferma, con la spia tanica accesa, prima di iniziare a funzionare.
	</p>
	<p>
		Si consiglia, ove possibile, di centrifugare la biancheria alla massima velocit&agrave; prevista per il tipo di tessuto, onde ridurre i tempi di asciugatura e di conseguenza il consumo di energia elettrica. La durata dell'asciugatura dipende dal tipo e quantit&agrave; di biancheria e dall'umidit&agrave; residua dopo la centrifuga. Alcuni tessuti,come la spugna, trattengono pi&ugrave; acqua rispetto ad altri; tenerne conto nell'impostazione del tempo di asciugatura. I tempi di asciugatura sono dati a titolo indicativo. Solo con l'esperienza acquisita dopo alcuni cicli di asciugatura si potr&agrave; essere in grado di stabilire i tempi pi&ugrave; adatti in base ai carichi di biancheria abituali. Non selezionare un tempo di asciugatura troppo lungo al primo utilizzo. &Egrave; preferibile impostare un tempo supplementare se la biancheria non &egrave; completamente asciutta. Se un carico &egrave; composto da tessuti di natura diversa (sintetici e cotone, ad esempio) selezionare il tempo di asciugatura in base alla fibra pi&ugrave; delicata e prolungarlo di 10 minuti.
	</p>
	<b>Consigli utili per l'asciugatura</b>
	<p>
		Non sovraccaricare la macchina. Almeno per le prime volte, pesare i carichi di biancheria. Il carico ottimale, pi&ugrave; economico, &egrave; comunque il carico massimo previsto per i vari tipi di tessuto.
	</p>
	<ul>
		<li>
			Introdurre sempre biancheria ben centrifugata. Pi&ugrave; alta &egrave; la velocit&agrave; di centrifuga, pi&ugrave; bassi saranno il consumo di energia elettrica e il tempo di asciugatura.
		</li>
		<li>
			Non lasciare ammassata troppo a lungo la biancheria umida da asciugare.
		</li>
		<li>
			Dividere la biancheria; il cotone e il lino si asciugano separatamente dai sintetici.
		</li>
		<li>
			Chiudere i bottoni automatici, le cerniere lampo e fissate i bottoni penzolanti.
		</li>
		<li>
			Rivoltare le tasche dei pantaloni ed i capi imbottiti per evitare tempi lunghi di asciugatura.
		</li>
		<li>
			Non asciugare nella macchina:
			<ul>
				<li>
					tessuti puliti a secco o con solventi chimici;
				</li>
				<li>
					tessuti di lana o misto lana che si restringono con il calore;
				</li>
				<li>
					tessuti molto delicati come la seta e le tendine sintetiche;
				</li>
				<li>
					capi in gommapiuma;
				</li>
				<li>
					capi in fibra di vetro.
				</li>
			</ul>
		</li>
		<li>
			Le etichette applicate ai tessuti possono essere d'aiuto.
		</li>
		<li>
			Un'eccessiva asciugatura, oltre ad essere antieconomica, rende pi&ugrave; difficile l'eventuale stiratura e pu&ograve; sgualcire i tessuti.
		</li>
		<li>
			Ritirare la biancheria non appena la macchina ha terminato il programma di asciugatura.
		</li>
		<li>
			Se i capi sintetici, dopo l'asciugatura, risultano carichi di elettricit&agrave; statica, usare un ammorbidente nell'ultimo   risciacquo.
