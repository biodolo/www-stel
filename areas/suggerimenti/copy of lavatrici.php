<h3>La maniglia della mia lavatrice e&grave; guasta</h3>
<div>
	<p>La maniglia potrebbe sembrare guasta quando a fine programma non ci permette l'apertura dello sportello.</p>
        <p>Si tratta di una caratteristica standard di sicurezza della serratura, la quale si sblocca dopo 2-5 minuti. Se dopo 2-5 minuti la maniglia non permette ancora l'apertura, controllare l'eventuale presenza di danni visibili su una delle parti della maniglia. E' da notare che alcuni modelli di lavatrice non consentono l'apertura dell'oblò se non avviene lo scarico completo dell'acqua.</p> 
        <p>Se non è possibile scaricare l'acqua oppure se sono presenti danni alla maniglia, &egrave; necessario l'intervento di un tecnico.</p>
</div>

<h3>Lo sportello della lavatrice non si apre</h3>
<div>
	<p>La serratura si sblocca dopo 2-5 minuti dal termine del ciclo. Alcuni modelli di lavatrice non consentono l'apertura dell'oblò se non avviene lo scarico completo dell'acqua.</p>
        <p>Se il ciclo di lavaggio è stato interrotto prima del termine provvedere allo scarico manuale dell'acqua ed attendere 2 – 5 minuti prima di tentare la nuova apertura. </p>
        <p>Se lo sportello comunque non si apre &egrave; necessario l'intervento di un tecnico.</p>
</div>

<h3>Odori sgradevoli</h3>
<div>
	<p>Possibili cause e soluzioni</p>
<ul>
        <li> Uso eccessivo di detersivo liquido con i programmi di lavaggio a freddo.
                <p>Dosare la quantità corretta di detersivo liquido ed evitare l'uso frequente di programmi di lavaggio a freddo.</p>
        </li>
        <li> Sovra dosaggio del detersivo e/o ammorbidente. 
                <p>Dosare la quantità corretta di detersivo e di ammorbidente, seguendo le indicazioni del produttore. </p>
        </li>
        <li> Lo sportello dell'apparecchiatura viene tenuto chiuso dopo ogni programma. 
                <p>Lasciare sempre lo sportello leggermente aperto dopo ogni ciclo.  </p>
        </li>
</ul>
