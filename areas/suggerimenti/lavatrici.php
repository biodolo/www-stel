<h3>La lavatrice vibra</h3>
<div>
	<p>
		Le vibrazioni della lavabiancheria possono essere causate dalla non completa rimozione degli imballaggi, dal carico di biancheria sbilanciato, o dal filtro sporco, o dal non corretto livellamento.
	<p>
	<p>
		Controlla di aver tolto i supporti di polistirolo espanso nella lavabiancheria, se non lo hai fatto consulta il libretto istruzioni e segui le indicazioni relative.
	</p>
	<p>
		Controlla di aver estratto i perni / viti di bloccaggio della vasca come indicato sul libretto istruzioni.
	</p>
	<p>
		Livella accuratamente la macchina svitando o avvitando i piedini regolabili. Non mettere mai sotto la macchina cartone, legno o materiali simili per compensare eventuali disuguaglianza del pavimento.
	</p>
	<p>
		Controlla il carico, se la biancheria &egrave; troppo poca potrebbe arrotolarsi nel cestello, il sistema frenante non sarebbe in grado di assorbire le oscillazioni.
	</p>
	<p>
		Scarica la lavabiancheria ed effettua un nuovo lavaggio con un carico completo. Controlla che il filtro non sia sporco.
	</p>
</div>

<h3>Uso quotidiano</h3>
<div>
	<p>
		La lavabiancheria &egrave; destinata ad un uso domestico. Non utilizzarla per scopi diversi da quelli per i quali &egrave; stata stata progettata.
		<ul>
			<li>
				Lavare solo articoli previsti per il lavaggio in macchina. Seguire le istruzioni riportate sull’etichetta di ogni capo.
			</li>
			<li>
				Non sovraccaricare la macchina. Seguire le istruzioni fornite nel manuale.
			</li>
			<li>
				Le monete, gli spilli di sicurezza, gli spilli, le viti, ecc. rimasti nella biancheria possono provocare grossi danni.
			</li>
			<li>
				Non si devono mettere in macchina capi smacchiati con benzina, alcool, trielina, ecc. Se si utilizzano tali prodotti prima del lavaggio,  bisogna aspettare che il prodotto sia evaporato prima di introdurre i capi nella macchina.
			</li>
			<li>
				Raggruppare i capi di piccole dimensioni, quali calzini, cinture, ecc., in una piccola borsa di tela o in una federa. Questo per evitare che questi pezzi vadano a finire tra il cesto e la vasca.
			</li>
			<li>
				Utilizzare solo la quantit&agrave; di ammorbidente indicata dal produttore. Una quantit&agrave; eccessiva di prodotto potrebbe rovinare la biancheria.
			</li>
			<li>
				Lasciare l’obl&ograve; semi-aperto quando non si utilizza la macchina. Questo permette una migliore conservazione della guarnizione dell’obl&ograve; e previene gli odori di muffa.
			</li>
			<li>
				Prima di aprire l’obl&ograve;, controllare sempre che l’acqua sia stata scaricata. In caso contrario, scaricarla seguendo le istruzioni fornite nel manuale d’uso.
			</li>
			<li>
				Si consiglia di staccare sempre la spina dalla presa di corrente e di chiudere il rubinetto dell’acqua dopo l’uso.
			</li>
			<li>
				In caso di guasto, non cercare di riparare la macchina da soli. Le riparazioni eseguite da personale non qualificato possono provocare danni.
			</li>
		</ul>

		Attenersi ai simboli per il trattamento dei tessuti sulle etichette dei singoli capi e seguire le istruzioni per il lavaggio del produttore. Suddividere la biancheria come segue: capi bianchi, colorati, sintetici, delicati, lana. I capi bianchi e colorati non devono essere lavati assieme. I capi bianchi possono perdere il candore con il lavaggio. La biancheria colorata nuova pu&ograve; stingere al primo lavaggio; &egrave; consigliabile quindi lavarla a parte, la prima volta. Accertarsi che nella biancheria non rimangano oggetti di metallo (es. fermagli, spille di sicurezza, spilli). Abbottonare le federe, chiudere le cerniere, i ganci e le asole. Legare eventuali cinture e nastri lunghi. Eliminare le macchie persistenti prima del lavaggio. Eventualmente strofinare i punti particolarmente sporchi con un detersivo speciale o con una pasta detergente. Trattare le tende con particolare cura. Togliere i ganci dalle tende o legarli in una retina o in un sacchetto. utilizzo ottimale di acqua ed energia. In caso di biancheria molto sporca, ridurre le quantit&agrave;.

		È possibile che macchie particolari non si eliminino solo con acqua e detersivo. È consigliabile, quindi, trattarle prima del lavaggio. Sangue: le macchie ancora fresche devono essere trattate con acqua fredda. In caso di macchie gi&agrave; asciutte, lasciare a mollo la biancheria per una notte con un detersivo speciale e poi strofinarla nell’acqua e sapone. Colore ad olio: inumidire con benzina detergente, tamponare le macchie dopo aver sistemato il capo su un panno morbido, trattarle pi&ugrave; volte con il detergente. Macchie asciutte di grasso: inumidire con trementina, tamponare le macchie con la punta delle dita e un panno di cotone, dopo aver appoggiato il capo su un ripiano morbido. Ruggine: sale ossalico (reperibile in drogheria) sciolto a caldo, o un prodotto antiruggine a freddo. Prestare attenzione alle macchie di ruggine non recenti, poiché la struttura di cellulosa &egrave; gi&agrave; stata intaccata e il tessuto tende a bucarsi. Macchie di muffa: trattarle con candeggina, sciacquare bene (solo capi bianchi e colorati resistenti al cloro). Erba: insaponare leggermente e trattare con candeggina diluita (capi bianchi, resistenti al cloro). Penna a biro e colla: inumidire con acetone , tamponare le macchie dopo aver appoggiato il capo su un panno morbido. Rossetto: inumidire con acetone come sopra indicato, poi trattare le macchie con alcool. Eliminare i residui sui tessuti bianchi con candeggina. Vino rosso: lasciare a mollo con del detersivo, risciacquare e trattare con acido acetico o acido citrico, poi risciacquare. Trattare eventuali residui con candeggina. Inchiostro: a seconda della composizione dell’ inchiostro, inumidire il tessuto prima con acetone , poi con acido acetico; trattare eventuali residui sui tessuti bianchi con candeggina e poi risciacquare bene. Macchie di catrame: trattare prima con smacchiatore, alcool o benzina, alla fine strofinare con pasta detergente. non usare l’acetone sulla seta artificiale.
	</p>
</div>

