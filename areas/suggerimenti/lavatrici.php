<h3>PULIZIA DELLA LAVATRICE</h3>
<div>
	<p>
		<i><b>Prima di eseguire qualunque intervento di manutenzione, spegnere l'apparecchiatura ed estrarre la spina dalla presa elettrica. Non scollegare alcun componente elettrico o funzionale, senza prima aver consultato Electrolux in quanto ciò potrebbe invalidare la garanzia.</b></i>
	</p>
	<p>
		Si consiglia di pulire l'apparecchiatura semplicemente con acqua e sapone. Infatti i detergenti possono reagire con gli ammorbidenti, graffiando i componenti di plastica e rendendoli fragili. Vedere anche le Istruzioni utente per la manutenzione dell'apparecchiatura. Le informazioni riportate di seguito servono da guida:
	</p>
	<p>
		Avviare un ciclo di lavaggio a 95 °C con detergente per lavatrice per evitare la formazione di odori e di macchie intorno allo sportello. 
	</p>
	<p>
		Per la corretta pulizia del filtro, seguire le istruzioni riportate di seguito:
		<ol>
			<li>
				Spegnere l'apparecchiatura;
			</li>
			<li>
				Aprire lo sportello del vano filtro e posizionarvi sotto un contenitore raccogli acqua;
			</li>
			<li>
				Scaricare l'acqua dalla macchina aprendo i tubi flessibili di scarico accanto al filtro;
			</li>
			<li>
				Rimuovere il filtro ruotandolo (con fermezza) in senso antiorario;
			</li>
			<li>
				Pulire il filtro e l'alloggiamento della pompa sciacquandoli sotto l'acqua corrente, quindi reinstallare il filtro;
			</li>
			<li>
				Chiudere il tubo flessibile di scarico e riposizionarlo nella sua posizione originale;
			</li>
			<li>
				Chiudere lo sportello del vano filtro.
			</li>
		</ol>
