<h3>Cattivo odore nel cestello</h3>
<div>
	<p>
		<i>Cattivo odore nel cestello con presenza, o senza, muffa sulla guarnizione obl&ograve;.</i>
		<ul>
			<li>
				Il problema &egrave; causato da residui di detersivo non sciolto per l'uso frequente, o costante, di programmi a bassa temperatura (0 - 40 °), che non consentono al detersivo di sciogliersi completamente.
			</li>
			<li>
				Effettuare uno pi&ugrave; (fino alla scomparsa del problema) lavaggi, a vuoto, a 90° con una forte dose di Calfort (o prodotto analogo). In alternativa possono essere usati prodotti anticalcare specifici per la disincrostazione delle lavatrici. In quest'ultimo caso seguire le istruzioni in etichetta.
			</li>
			<li>
				Se intendi continuare a lavare a bassa temperatura preferisci detersivi liquidi e, almeno una volta all'anno, effettua il trattamento suggerito.
			</li>
		</ul>
	</p>
