<h3>Manutenzione</h3>
<div>
	<p>
		<i>Mobile</i>
	</p>
	<p>
		Pulire il mobile della macchina con acqua tiepida e un detersivo neutro non abrasivo. Sciacquare con acqua pulita e asciugare con uno strofinaccio morbido. Importante: non usare alcool, solventi o prodotti similari per la pulizia del mobile.
	</p>
	<i>Cassetto detersivo</i>
	<p>
		I detersivi e gli additivi formano, a lungo andare, delle incrostazioni. Pulire il cassetto detersivo di tanto in tanto, passandolo sotto il rubinetto dell’acqua. Per estrarre completamente il cassetto dalla macchina, premere sul tasto in fondo a sinistra. Per poter pulire pi&ugrave; facilmente il cassetto, &egrave; possibile togliere la parte alta delle vaschette riservate agli additivi. A lungo andare, il detersivo pu&ograve; formare incrostazioni nella sede del cassetto: pulire con un vecchio spazzolino da denti. Rimettere il cassetto al suo posto.
	</p>
	<i>Filtro di scarico dell’acqua</i>
	<p>
		Il ruolo del filtro consiste nella raccolta di filacci e di piccoli oggetti lasciati inavvertitamente nella biancheria. Controllare periodicamente che il filtro sia pulito. Aprire lo sportellino. Mettere un recipiente sotto il filtro e svitarlo; &egrave; normale che fuoriesca una piccola quantit&agrave; d’acqua. Estrarre il filtro. Pulirlo sotto il rubinetto e rimetterlo al suo posto riavvitandolo a fondo.
	</p>
	<i>Filtrino del tubo di alimentazione</i>
	<p>
		Se ci si accorge che la macchina richiede pi&ugrave; tempo per riempirsi, bisogna verificare che il filtrino del tubo di alimentazione non sia ostruito. Chiudere il rubinetto delI’acqua. Svitare il tubo dal rubinetto e togliere il filtrino. Pulirlo con uno spazzolino. Rimetterlo al suo posto e riavvitare il tubo.
	</p>
	<i>Scarico di emergenza dell’acqua</i>
	<p>
		Se la macchina non scarica (pompa bloccata, filtro intasato o tubo di scarico intasato), procedere come segue per togliere l’acqua dalla vasca:
	</p>
	<ul>
		<li>
			togliere la spina dalla presa di corrente
		</li>
		<li>
			chiudere il rubinetto dell’acqua n aspettare che l’acqua si raffreddi (nel caso fosse in corso un programma con   temperatura di 60°C o pi&ugrave;)
		</li>
		<li>
			prendere un recipiente di capienza adatta (circa 15-20 litri), porlo sul pavimento, inserire all’interno l’estremit&agrave;   del tubo di scarico e lasciare scaricare tutta l’acqua dalla vasca n procedere quindi alla rimozione del guasto e   rimettere a posto la macchina.
		</li>
	</ul>
	<p>
		Qualora l’acqua non fuoriesca, perch&egrave; il filtro o il tubo di scarico &egrave; intasato, o non fosse possibile accedere al tubo di scarico, procedere come segue:
	</p>
	<ul>
		<li>
			aprire lo sportello del filtro e porre sotto lo stesso un recipiente con bordi diritti e di capienza adatta n svitare il   filtro in senso antiorario quanto basta per fare fuoriuscire lentamente tutta l’acqua dalla vasca.Procedere successivamente come sopra.
		</li>
	</ul>
</div>
<h3>Non funziona?</h3>
<div>
	<p>
		<i>La macchina non si mette in moto</i>

		<ul>
			<li>
				Controllare che l’obl&ograve; sia ben chiuso.
			</li>
			<li>
				Controllare se la macchina riceve corrente.
			</li>
			<li>
				Controllare se il fusibile dell’impianto domestico &egrave; intatto.
			</li>
			<li>
				Controllare che la manopola selettore programmi sia posizionata correttamente e il tasto avvio/arresto premuto.
			</li>
		</ul>
	</p>

	<p>
		<i>La macchina non carica acqua.</i>
		<ul>
			<li>
				Verificare se il rubinetto dell’acqua &egrave; aperto.
			</li>
			<li>
				Controllare se l’acqua esce dal rubinetto.
			</li>
			<li>
				Verificare che il tubo di alimentazione non sia incastrato o piegato.
			</li>
			<li>
				Controllare che il filtrino del tubo di alimentazione non sia ostruito.
			</li>
			<li>
				Verificare che l'obl&ograve; sia chiuso.
			</li>
		</ul>
	</p>

	<p>
		<i>La macchina carica acqua e la scarica immediatamente</i>
		<ul>
			<li>
				L’estremit&agrave; del tubo di scarico si trova troppo in basso rispetto alla macchina.
			</li>
		</ul>
	</p>
	<p>
		<i>La macchina non scarica e/o non centrifuga</i>
		<ul>
			<li>
				Verificare che il tubo di scarico non sia incastrato o piegato.
			</li>
			<li>
				Verificare che il tubo di scarico e il filtro non siano intasati.
			</li>
			<li>
				È stata scelta l’opzione «centrifuga esclusa» o «antipiega».
			</li>
		</ul>
	</p>
	<p>
		<i>C’&egrave; acqua per terra </i>
		<ul>
			<li>
				C’&egrave; troppo detersivo o &egrave; stato usato un detersivo non adatto (fa troppa schiuma).
			</li>
			<li>
				Verificare se ci sono perdite da una delle ghiere del tubo di alimentazione. È difficile vedere l’acqua correre lungo    il tubo,   controllare se &egrave; umido.
			</li>
			<li>
				Controllare che il tubo di scarico sia integro.
			</li>
		</ul>
	</p>
	<p>
		<i>Il risultato del lavaggio non &egrave; soddisfacente </i>
		<ul>
			<li>
				È stato usato poco detersivo o un detersivo non adatto.
			</li>
			<li>
				Le macchie “difficili” non sono state sottoposte ad un trattamento preliminare
			</li>
			<li>
				Non &egrave; stata scelta la temperatura giusta, abbinata al programma corretto.
			</li>
		</ul>
	</p>

	<p>
		<i>La macchina vibra o fa rumore</i>
		<ul>
			<li>
				Verificare se sono state tolte tutte le protezioni che servivano per il trasporto.
			</li>
			<li>
				Controllare che la macchina non sia appoggiata contro qualche cosa.
			</li>
			<li>
				Verificare se i piedini di sostegno appoggiano bene al suolo.
			</li>
			<li>
				Controllare che il carico di biancheria sia ben distribuito nel cesto.
			</li>
			<li>
				Forse c’&egrave; poca biancheria nel cesto.
			</li>
		</ul>
	</p>
	<p>
		<i>L’obl&ograve; non si apre</i>
		<ul>
			<li>
				Il programma &egrave; ancora in corso.
			</li>
			<li>
				L’obl&ograve; non si &egrave; ancora sbloccato.
			</li>
			<li>
				Scaricare l’acqua in quanto &egrave; stato scelto un programma “delicato” con l’opzione “antipiega” che termina con   l’acqua in vasca.
			</li>
		</ul>
	</p>
	<p>
		<i>La centrifuga inizia in ritardo o non viene effettuata</i>
		<ul>
			<li>
				E’ intervenuta “la sicurezza antisbilanciamento" che previene vibrazioni eccessive in centrifuga ed assicura stabilit&agrave;: se all'inizio della fase di centrifuga la biancheria non &egrave; uniformemente distribuita all'interno del cesto, la  macchina lo riconosce e cerca di ridistribuire il bucato in maniera omogenea, attraverso alcune rotazioni del cesto. Solo quando il bucato si &egrave; distribuito in maniera uniforme, viene eseguita la fase di centrifuga. Se dopo alcuni tentativi la biancheria continua a rimanere appallottolata, la macchina salta la fase di centrifuga.Quindi,   qualora a fine ciclo la biancheria rimanga bagnata, consigliamo di ridistribuirla manualmente all'interno del cesto e di selezionare nuovamente il programma di centrifuga.
			</li>
		</ul>
	</p>
</div>
<h3>Ha allagato?</h3>
<div>
	<p>
		<i>La fuoriuscita di acqua pu&ograve; avvenire dalla macchina, dai tubi di carico e scarico acqua, o dai raccordi col rubinetto o attacco di scarico a muro.</i>
		<ul>
			<li>
				Controlla che il filtro sia fissato correttamente e ben chiuso. Se necessario provvedi alla pulizia della zona di scarico.
			</li>
			<li>
				Prova a toccare i tubi di carico e scarico per sentire se sono bagnati. In questo caso potrebbero essere rotti, contata il servizio assistenza per la loro sostituzione. Controlla con la mano anche il rubinetto e l'attacco dello scarico, se sono bagnati prova a stringerli.
			</li>
			<li>
				Stacca la spina, estrai e lava accuratamente la vaschetta detersivo, tutti i suoi componenti, e il suo alloggiamento nella macchina.
			</li>
		</ul>
	</p>
</div>
