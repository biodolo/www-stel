<h3>Fumo dal forno</h3>
<div>
	<p>Per prima cosa controllate che non sia necessario pulire il forno, potrebbero esserci resti di cibo e quindi bruciare, provocando fumo.</p>
    <p>Verificate che del cibo non fuoriesca dal contenitore, o che non vi siano troppi schizzi di grasso/sugo sulle pareti del forno.</p>
<p>Infine verificate che la temperatura sia adeguata, in particolare nel caso di cottura di carni.</p>
<p>Dopo ogni cottura, pulisci le pareti del forno con una spugna e un detergente delicato per togliere gli schizzi di sostanze grasse.</p>
<p>Controlla anche le resistenze, se noti dei residui toglili usando una spugnatta asciutta. Addottando questa precauzione, il tuo forno produrr&amp;agrave; meno fumo durante la successiva cottura.</p>
</div>

