<h3>Pulizia e manutenzione</h3>
<div>
	<p>Prima di qualsiasi intervento di manutenzione o di pulizia, verificate che l’apparecchiatura sia disinserita dalla rete elettrica.</p>
    <center><strong>Il piano di cottura </strong></center>
    <p>Lavate le griglie smaltate con acqua e detersivo per stoviglie. Potete lavarle anche nella lavastoviglie. Togliete i cappellotti e gli spartifiamma e lavateli accuratamente con acqua calda e detersivo per stoviglie. Asciugateli bene prima di rimetterli a posto e controllate che siano collocati nella corretta posizione. Rimuovete prima possibile tutte le tracce di salsa, sugo, zucchero bruciato, usando uno strofinaccio umido, perché i condimenti e le sostanze zuccherine possono far perdere brillantezza allo smalto. Non utilizzate pagliette o coltelli per togliere le incrostazioni. Evitate sempre l'uso di prodotti abrasivi. Nei modelli dotati di accensione automatica dei fornelli, questa funzione &amp;egrave; assicurata dalla presenza di una "candeletta" in ceramica ed un elettrodo in metallo. Procedete periodicamente ad una accurata pulizia di queste parti del piano. Inoltre, per evitare difficolt&amp;agrave; di accensione, controllate che i fori della corona spartifiamma non siano otturati.</p>

    <center><strong>Le piastre elettriche </strong></center>
<p>Pulite le piastre elettriche quando sono fredde, usando un po' di carta assorbente od un panno umido che non lasci filamenti. Asciugate accuratamente le piastre prima di usarle di nuovo. Di tanto in tanto, Vi consigliamo di ungerle con un po' di olio alimentare o vaselina, mentre sono ancora tiepide.</p>

    <center><strong>Il forno </strong></center>
<p>Per la pulizia giornaliera utilizzate acqua calda e detersivo delicato o uno dei tanti prodotti esistenti in commercio. Non utilizzate pagliette o coltelli per togliere le incrostazioni. Evitate sempre l'uso di prodotti abrasivi. Se utilizzate prodotti spray, non dirigete lo spruzzo sulle parti in acciaio inossidabile, perché potreste danneggiarle. Pulire gli accessori del forno (griglia, leccarda, ecc.) con acqua calda e detersivo. Pulite la porta forno solo con acqua calda, evitate l’uso di panni ruvidi o sostanze abrasive. Evitate di usare fogli di alluminio per coprire le pareti o il fondo del forno allo scopo di raccogliere gli spruzzi di grasso. Questo comportamento può causare un accumulo di calore e quindi danneggiare lo smalto.</p>

    <center><strong>Controlli periodici </strong></center>
<p>Fate controllare periodicamente lo stato di conservazione del tubo flessibile di collegamento gas e fatelo sostituire da personale qualificato appena presenta qualche anomalia. Vi consigliamo di sostituirlo una volta all'anno. Fate lubrificare periodicamente i rubinetti di regolazione del gas da personale qualificato. Se notate anomalie nel funzionamento &amp;egrave; opportuno far controllare la cucina da personale qualificato.</p>
</div>

<h3>Suggerimenti</h3>
<div>
	<p>Come utilizzare al meglio il forno</p>


    <center><strong>Per la cottura di carni e pesce </strong></center>
<p>Le carni possono essere sistemate in recipienti adatti alla cottura in forno, oppure direttamente sulla griglia. In questo secondo caso, versate SEMPRE un po’ d’acqua nella leccarda ed inseritela nelle guide sotto la griglia. La leccarda raccoglier&amp;agrave; i grassi che si sciolgono durante la cottura e l’acqua eviter&amp;agrave; che i grassi brucino, formando cattivi odori o fumo. Le carni bianche, i volatili ed il pesce richiedono in genere una cottura a media temperatura (tra 150 e 175 gradi), mentre la cottura “al sangue” di carni rosse richiede temperature fra 200 e 250 gradi per tempi brevi.</p>

    <center><strong>Per la cottura di dolci </strong></center>
<p>I dolci hanno bisogno di una temperatura moderata, da 150 a 200 gradi. Prima di iniziare la cottura di un dolce, fate sempre riscaldare il forno per almeno 10 minuti, ed una volta iniziata la cottura, tenete sempre chiusa la porta del forno.

    <center><strong>Per la cottura al grill </strong></center> 
<p>Se desiderate cuocere carne o pesce al grill, ungeteli leggermente e collocateli sulla griglia. Nella cottura al grill il calore proviene solo dall’alto, perciò la griglia va inserita nelle guide superiori o inferiori in base allo spessore della carne o del pesce. Anche in questo caso, ricordate SEMPRE di inserire la leccarda nelle guide più basse, dopo avervi versato circa due bicchieri d’acqua.

    <center><strong>Griglie e leccarda </strong></center>
<p>La griglia e la leccarda fornite in dotazione nelle cucine vanno posizionate all'interno del forno su livelli separati.

    <center><strong>Come scegliere il recipiente adatto </strong></center>
<p><ul>
<li> I recipienti in alluminio, in terracotta o in pirex riducono la brunitura sotto le Vostre pietanze. Per questo sono consigliabili per la cottura di dolci lievitati o di arrosti.</li>
<li>I recipienti in ghisa smaltata, in porcellana resistente al calore, oppure i recipienti con superficie antiaderente e olorati all'esterno, aumentano la brunitura sotto le Vostre pietanze, e sono perciò consigliabili per la cottura di pizze, di crostate, e in genere di tutti quei piatti che devono richiedono una doratura uniforme sia sopra che sotto.</li>
</ul></p>
</div>

<h3>Non funziona</h3>
<div>
	<p><i>Cosa fare se la cucina non funziona</i></p>
    <center><strong>La fiamma non &amp;egrave; regolare o tende a spegnersi. </strong></center>
<p>Controllate che: 
<ul>
<li>i fori degli spartifiamma dei bruciatori non siano ostruiti;</li>
<li>il regolatore di pressione sia funzionante; </li>
<li>nel caso di gas proveniente da una bombola, che la stessa contenga ancora del gas; </li>
<li>il rubinetto della bombola sia completamente aperto.</li>
</ul>
</p>

    <center><strong>Si avverte odore di gas nella stanza.  </strong></center>
<p>Controllate che: 
<ul>
<li>un rubinetto non sia rimasto aperto; </li>
<li>il tubo di alimentazione del gas sia ben posizionato e in buono stato; ricordatevi di sostituirlo almeno una volta     all’anno. Evitate assolutamente di cercare fughe di gas con un fiammifero! Se avete dubbi, chiudete il rubinetto     generale del gas e rivolgeteVi ad un centro di Assistenza autorizzato.</li>
</ul></p>

    <center><strong>Il forno non scalda. </strong></center>
<p> AssicurateVi che la manopola del forno e del timer di cottura siano in posizione di funzionamento.</p>

    <center><strong>I tempi di cottura sono troppo lunghi. </strong></center>
<p>Controllate che la temperatura impostata sia quella più adatta all’alimento da cuocere.</p>

    <center><strong>Il forno emette fumo.  </strong></center>
<p>Vi consigliamo di pulirlo dopo ogni uso. Durante la cottura della carne si formano degli spruzzi di grasso che, possono causare fumo ed cattivo odore alla cottura successiva, se non vengono immediatamente puliti.</p>

    <center><strong>La luce del forno non funziona. </strong></center>
<p>La lampada del forno può essere bruciata. Per la sua sostituzione seguite le indicazioni fornite nel libretto    istruzioni.</p>
</div>

