	<p>
		I settori di attivit&agrave; vengono notevolmente potenziati aumentando l'offerta agli utenti rendendo
		cos&igrave; la S.T.EL. snc un vero punto di riferimento. Ai tradizionali settori merceologici vengono aggiunti
		gli elettromestici da incasso e una vasta scelta di materiale elettrico. Negli anni 2003 e 2004 vengono stretti accordi
		di collaborazione con professionisti esterni rendendo pi&ugrave; versatile, efficiente e puntuale il servizio.
	</p>
	<p>
		S.T.EL. snc fin dalla formazione svolge servizio di assistenza tecnica autorizzata per i marchi del gruppo
		ELECTROLUX ITALIA (allora Zanussi s.p.a.). Il trasferimento presso l'attuale sede coincide con l'inizio del servizio di
		assistenza per i marchi del gruppo OCEAN a cui si affiancano successivamente altri marchi prestigiosi.
		A met&agrave; degli anni 90 l'azienda inizia ad operare nel settore climatizzazione ed attualmente svolge servizio di
		assistenza tecnica autorizzata per ARGOCLIMA, SAMSUNG, PANASONIC, OLIMPIA SPLENDID e altri marchi.
		Contemporaneamente inizia l'attivit&agrave; nel settore dei piccoli elettrodomestici assistendo marchi come
		GISOWATT,&nbsp;POLTI, MOEL, BIMAR ai quali ben presto se ne affiancano altri. Nel 2007 l'azienda inizia ad operare
		nel settore trattamento acqua assistendo e installando addolcitori WATERBOSS e rubinetto 4 SPRINGS di ELECTROLUX.
		Dal 2008 S.T.EL. snc &egrave; anche assistenza per tutti i prodotti VORTICE. A maggio 2010 diventa S.T.EL. SRL con la
		fusione con Delta Service, affermata azienda del settore climatizzazione. L'operazione accresce notevolmente le
		potenzialit&agrave; nel settore dell'impiantistica e porta all'acquisizione di nuovi marchi di climatizzatori: TOSHIBA,
		MITSUBISHI HEAVY, SANYO. Nel 2011 le mutate esigenze organizzative e di gestione dell'attivit&agrave; sul territorio di
		competenza, inducono alla chiusura della filiale di Massa col trasferimento dell'attivit&agrave; presso la sede di Viareggio.
		Ne nasce una struttura particolarmente efficiente, dinamica e moderna con i processi interamente informatizzati.
		L'aumentata superficie di vendita consente ora di offrire a utenti e clienti una gamma davvero completa di prodotti e
		accessori
	</p>
