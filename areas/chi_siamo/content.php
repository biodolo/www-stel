<h2>CHI SIAMO</h2>
<div align="justify">
	<p>
		<b>S.T.EL. snc (Servizio Tecnico ELettrodomestici)</b> nasce nel Febbraio del 1986 a Viareggio, al termine della gestione diretta del servizio di assistenza tecnica, e la chiusura della filiale della <strong> LINO ZANUSSI s.p.a.</strong> di Viareggio, acquisendone l'appalto per i comuni di attuale competenza.
	</p>
