			Ieri
		</figcaption>
	</figure>
	<p>
		&Egrave; composta da tre elementi, ex tecnici della Zanussi.
		Dopo  le prime fasi di rodaggio si manifesta l'esigenza di potenziare l'organico con l'inserimento di una segretaria addetta alla
		ricezione delle chiamate e di un ulteriore tecnico; la sede &egrave; in via Puccini, centro citt&agrave;.
		Ben presto la sede di via Puccini diventa inadatta.
	</p>
	<p>
		L'attivit&agrave; si trasferisce allora in via Alessandro Volta (ora via Montesagro, 19) in un locale pi&ugrave;
		ampio, l'organico viene ulteriormente potenziato.
		Al tradizionale settore d'attivit&agrave; legato ai grandi elettrodomestici si affiancano i climatizzatori e, successivamente,
		i piccoli elettrodomestici. Anche l'offerta di ricambi viene incrementata con la disponibilit&agrave; di ricambi,
		originali e adattabili, di quasi tutte le marche. La fine degli anni novanta evidenzia la necessit&agrave; di un
		punto di riferimento sulla zona di Massa Carrara, divenute nel frattempo citt&agrave; strategiche per l'attivit&agrave;
		aziendale. Nell'ottobre del 1998 viene aperto il negozio di Massa, in via Aurelia Ovest, 4.
	</p>
